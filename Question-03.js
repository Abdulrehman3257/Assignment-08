// Write a js program to check whether a number is negative, positive or
// zero.

let number = 2;
if (number < 0) {
    console.log("Number is Negative");
} else if (number == 0) {
    console.log("Number is Zero");
} else if (number > 0) {
    console.log("Number is Positive");
} else {
    console.log("Invalid");
}
